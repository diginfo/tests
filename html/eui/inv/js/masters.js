$( document ).ready(function() {
  
  $('form').on('loadDone',function(){
    var asdpx = $(this).attr('asdpx');
    butEn(asdpx+'s');
  })
  
  $('#TABLE_NAME').combobox({
    onSelect:function(rec){
      var cbo = $('#COLUMN_NAME');
      var url = '/?_func=get&_combo=y&_sqlid=dqm^columnname&TABLE_NAME='+rec.value;
      cbo.combobox('clear');
      cbo.combobox('reload',url);
    }
  })
  
  $('#lists').tabs({
    onSelect:function(tit,idx){
      tabcombos($(this));
      var frm = $(this).tabs('getSelected').find('form').first();
      frm.form('reset');
      setTimeout(function(){butEn(frm.attr('asdpx'));},100)
    }
  }) 
  
  // setTimeout(function(){$('#lists').tabs('unselect',0).tabs('select',0)});
  
});